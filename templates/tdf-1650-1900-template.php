                           <div class="features">
                              <div class="features__title">
                                 <h3>features of our products</h3>
                              </div>
                              <div class="grid-list">
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/01/user-friendly.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>User friendly</h4>
                                       <p>Which allows an easier access to all machine functions, by <strong>simplifying</strong> the settings, the machine activation and system check.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/rulli-traino-film-4.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>SCRAPS REWINDER</h4>
                                       <p>Rewinder with pneumatic expansion and increased stroke, to allow the scraps rewinding on both carton or directly on the roll. The group is controlled by clutch with adjustable strength to adapt it to every kind of film. The motor is positioned inside the electrical cabinet.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/motori-brushless-8.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>BRUSHLESS MOTORS</h4>
                                       <p>AII machine motion controlled by <strong>Brushless motors</strong>. These, through an electronic cams connection of the moving components, allow more flexibility in terms of films used and speed control, according to the products to be packed.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/gruppo-spingitori.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>PUSHER GROUP</h4>
                                       <p>Moved by nr. <strong>2 Servomotors controlled by electronic</strong> cam for trays taking, their positioning under the sealing mould and discharge of sealed trays. Their motor is positioned inside the machine cabinet.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/stazione-saldatura.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>SEALING STATION</h4>
                                       <p><strong>Servomotor</strong> controlled Lifting mechanism of the sealing station, this is managed by the same cam used for the pushers. Sealing strength to be set on the HMI.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/nastro-ingresso-passo-passo.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>INLET BELT</h4>
                                       <p>Servomotor controlled positioning system, for trays <strong>correct positioning</strong> at machine entrance. Made of <strong>Stainless steel</strong>, it is equipped with fast removal unlock system, to ease cleaning operations: no tools needed for belt handling.</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/10/igienicità.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>Hygienical features</h4>
                                       <p>Sloping machine surfaces, TIG continuous welding and motors strategically protected and positioned, Will grant the <strong>highest possible hygiene levels</strong>!</p>
                                    </div>
                                 </div>
                                 <div class="grid-list__item">
                                    <div class="grid-list__item__image">
                                       <img src="https://www.colimatic.com/wp-content/uploads/2019/01/risparmio.svg" class="attachment-full size-full">
                                    </div>
                                    <div class="grid-list__item__body">
                                       <h4>Extreme energy saving</h4>
                                       <p>The configuration with shared DC Bus allows the energy exchange between the axis, <strong>reducing the energy wasting</strong> caused by the dynamic braking resistances. This will sensibly reduce (down to 40%!) the amount of energy required.
                                          (<strong>KERS</strong> - Kinetic Energy Recovery System).
                                       </p>
                                    </div>
